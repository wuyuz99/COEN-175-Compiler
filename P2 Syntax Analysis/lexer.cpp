using namespace std;
int numerrors, lineno = 1;


/* Later, we will associate token values with each keyword */

static vector<pair<string, int>> keywords = {
    make_pair("auto", AUTO),
    make_pair("break", BREAK),
    make_pair("case", CASE),
    make_pair("char", CHAR),
    make_pair("const", CONST),
    make_pair("continue", CONTINUE),
    make_pair("default", DEFAULT),
    make_pair("do", DO),
    make_pair("double", DOUBLE),
    make_pair("else", ELSE),
    make_pair("enum", ENUM),
    make_pair("extern", EXTERN),
    make_pair("float", FLOAT),
    make_pair("for", FOR),
    make_pair("goto", GOTO),
    make_pair("if", IF),
    make_pair("int", INT),
    make_pair("long", LONG),
    make_pair("register", REGISTER),
    make_pair("return", RETURN),
    make_pair("short", SHORT),
    make_pair("signed", SIGNED),
    make_pair("sizeof", SIZEOF),
    make_pair("static", STATIC),
    make_pair("struct", STRUCT),
    make_pair("switch", SWITCH),
    make_pair("typedef", TYPEDEF),
    make_pair("union", UNION),
    make_pair("unsigned", UNSIGNED),
    make_pair("void", VOID),
    make_pair("volatile", VOLATILE),
    make_pair("while", WHILE)
};


/*
 * Function:	report
 *
 * Description:	Report an error to the standard error prefixed with the
 *		line number.  We'll be using this a lot later with an
 *		optional string argument, but C++'s stupid streams don't do
 *		positional arguments, so we actually resort to snprintf.
 *		You just can't beat C for doing things down and dirty.
 */

void report(const string &str, const string &arg)
{
    char buf[1000];

    snprintf(buf, sizeof(buf), str.c_str(), arg.c_str());
    cerr << "line " << lineno << ": " << buf << endl;
    numerrors ++;
}


/*
 * Function:	lexan
 *
 * Description:	Read and tokenize the standard input stream.  The lexeme is
 *		stored in a buffer.
 */

int lexan(string &lexbuf)
{
    static int c = cin.get();
    bool invalid, overflow;
    long val;
    int p;


    /* The invariant here is that the next character has already been read
       and is ready to be classified.  In this way, we eliminate having to
       push back characters onto the stream, merely to read them again. */

    while (!cin.eof()) {
	lexbuf.clear();


	/* Ignore white space */

	while (isspace(c)) {
	    if (c == '\n')
		lineno ++;

	    c = cin.get();
	}


	/* Check for an identifier or a keyword */

	if (isalpha(c) || c == '_') {
	    do {
		lexbuf += c;
		c = cin.get();
	    } while (isalnum(c) || c == '_');
        int token = ID;
        for (size_t i = 0; i < keywords.size(); ++i) {
            if(keywords[i].first == lexbuf){
                token = keywords[i].second;
                break;
            }
        }
	   return token;


	/* Check for a number */

	} else if (isdigit(c)) {
	    do {
		lexbuf += c;
		c = cin.get();
	    } while (isdigit(c));

	    errno = 0;
	    val = strtol(lexbuf.c_str(), NULL, 0);

	    if (errno != 0 || val != (int) val)
		report("integer constant too large");

	    return NUM;


	/* There must be an easier way to do this.  It might seem stupid at
	   this point to recognize each token separately, but eventually
	   we'll be returning separate token values to the parser, so we
	   might as well do it now. */

	} else {
	    lexbuf += c;

	    switch(c) {


	    /* Check for '||' */

	    case '|':
		c = cin.get();

		if (c == '|') {
		    lexbuf += c;
		    c = cin.get();
            return OR;
		}

		return VLINE;


	    /* Check for '=' and '==' */

	    case '=':
		c = cin.get();

		if (c == '=') {
		    lexbuf += c;
		    c = cin.get();
            return EQL;
		}

		return ASSIGN;


	    /* Check for '&' and '&&' */

	    case '&':
		c = cin.get();

		if (c == '&') {
		    lexbuf += c;
		    c = cin.get();
            return AND;
		}

		return ADDR;


	    /* Check for '!' and '!=' */

	    case '!':
		c = cin.get();

		if (c == '=') {
		    lexbuf += c;
		    c = cin.get();
            return NEQ;
		}

		return NOT;


	    /* Check for '<' and '<=' */

	    case '<':
		c = cin.get();

		if (c == '=') {
		    lexbuf += c;
		    c = cin.get();
            return LEQ;
		}

		return LTN;


	    /* Check for '>' and '>=' */

	    case '>':
		c = cin.get();

		if (c == '=') {
		    lexbuf += c;
		    c = cin.get();
            return GEQ;
		}

		return GTN;


	    /* Check for '-', '--', and '->' */

	    case '-':
		c = cin.get();

		if (c == '-') {
		    lexbuf += c;
		    c = cin.get();
            return DECR;
		} else if (c == '>') {
		    lexbuf += c;
		    c = cin.get();
            return ARROW;
		}

		return SUB;


	    /* Check for '+' and '++' */

	    case '+':
		c = cin.get();

		if (c == '+') {
		    lexbuf += c;
		    c = cin.get();
            return INCR;
		}

		return ADD;


	    /* Check for simple, single character tokens */

	    case '*': case '%': case ':': case ';':
	    case '(': case ')': case '[': case ']':
	    case '{': case '}': case '.': case ',':
		c = cin.get();
		return lexbuf[0];


	    /* Check for '/' or a comment */

	    case '/':
		c = cin.get();

		if (c == '*') {
		    do {
			while (c != '*' && !cin.eof()) {
			    if (c == '\n')
				lineno ++;

			    c = cin.get();
			}

			c = cin.get();
		    } while (c != '/' && !cin.eof());

		    c = cin.get();
		    break;

		} else {
		    return lexbuf[0];
		}


	    /* Check for a string literal */

	    case '"':
		do {
		    p = c;
		    c = cin.get();
		    lexbuf += c;

		    if (c == '\n')
			lineno ++;

		} while (p == '\\' || (c != '"' && c != '\n' && !cin.eof()));

		if (c == '\n' || cin.eof())
		    report("premature end of string literal");
		else {
		    parseString(lexbuf, invalid, overflow);

		    if (invalid)
			report("unknown escape sequence in string literal");
		    else if (overflow)
			report("escape sequence out of range in string literal");
		}

		c = cin.get();
		return STRING;


	    /* Handle EOF here as well */

	    case EOF:
		return DONE;


	    /* Everything else is illegal */

	    default:
		c = cin.get();
		break;
	    }
	}
    }

    return DONE;
}
